    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Select Profile Image");
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);

        int result = chooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = chooser.getSelectedFile();

            try {
                FileInputStream fis = new FileInputStream(selectedFile);
                byte[] imageData = fis.readAllBytes();

                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/nexamarket", "root", "1234");
                PreparedStatement ps = con.prepareStatement("UPDATE user SET profile_image=? WHERE id=?");
                ps.setBytes(1, imageData);
                ps.setString(2, user.getId());

                int updated = ps.executeUpdate();
                if (updated > 0) {
                    JOptionPane.showMessageDialog(this, "Profile image updated successfully.");

                  
                    File dest = new File("src/dev/ramindu/nexamarket/profileImage/" + user.getId() + ".jpg");
                    Files.copy(selectedFile.toPath(), dest.toPath(), StandardCopyOption.REPLACE_EXISTING);

                  
                    imageAvatar1.setImage(new ImageIcon(imageData));
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to update profile image.");
                }

                ps.close();
                con.close();

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed


