    private void loadUserData() {
        jTextField1.setText(user.getId());
        jTextField2.setText(user.getContact());
        jTextField3.setText(user.getFirstName() + " " + user.getLastName());
        jTextField4.setText(user.getUsername());
        jTextField5.setText(user.getEmail());
        jLabel7.setText(user.getFirstName() + " " + user.getLastName());
        jLabel8.setText(user.getEmail());

        try {
            Connection con = dev.ramindu.nexamarket.utils.DatabaseConnection.getConnection();
            PreparedStatement ps = con.prepareStatement("SELECT profile_image FROM user WHERE id=?");
            ps.setString(1, user.getId());

            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                byte[] imgData = rs.getBytes("profile_image");
                if (imgData != null) {
                    imageAvatar1.setImage(new ImageIcon(imgData));
                }
            }

            rs.close();
            ps.close();
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

